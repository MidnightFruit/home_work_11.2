def to_up(str):
    """
    Делает символы в строке заглавными
    :param str: входная строка
    :return:
    """
    return str.upper()

def first_to_up(string: str) -> str:
    """
    Делает так что бы каждое слово начиналось с заглавной буквы
    :param string: входная строка
    :return:
    """
    return string.title()